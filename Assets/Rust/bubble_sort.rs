// Bubblesort algorithm
pub fn bubble_sort_rust() {
    let mut arr: [i32; 4] = [4, 8, 3, 1];
    let mut new_len: usize;
    let mut len = arr.len();
    loop {
        new_len = 0;
        for i in 1..len {
            if arr[i - 1] > arr[i] {
                arr.swap(i - 1, i);
                new_len = i;
            }
        }
        if new_len == 0 {
            break;
        }
        len = new_len;
    }
}
